export function getQuestionsBySubject(subject: string): QuizQuestion[] {
  return QUESTION_BANK.filter((q) => q.subject === subject);
}

export function getQuestionsByTopic(subject: string, topic: string): QuizQuestion[] {
  return QUESTION_BANK.filter((q) => q.subject === subject && q.topic === topic);
}

export function getRandomQuestions(
  subject: string,
  topic: string,
  count: number,
  difficulty?: "easy" | "medium" | "hard"
): QuizQuestion[] {
  const pool = QUESTION_BANK.filter(
    (q) =>
      q.subject === subject &&
      q.topic === topic &&
      (difficulty ? q.difficulty === difficulty : true)
  );
  if (pool.length === 0) {
    // トピック一致なしの場合は分野だけで絞る
    const fallback = QUESTION_BANK.filter(
      (q) => q.subject === subject && (difficulty ? q.difficulty === difficulty : true)
    );
    return [...fallback].sort(() => Math.random() - 0.5).slice(0, count);
  }
  return [...pool].sort(() => Math.random() - 0.5).slice(0, count);
}
